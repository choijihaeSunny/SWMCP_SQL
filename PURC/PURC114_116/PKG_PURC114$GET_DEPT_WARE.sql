CREATE DEFINER=`ubidom`@`%` PROCEDURE `swmcp`.`PKG_PURC114$GET_DEPT_WARE`(	

	OUT N_RETURN INT,

	OUT V_RETURN VARCHAR(4000)

	)
begin

	DECLARE EXIT HANDLER FOR SQLEXCEPTION 

	CALL USP_SYS_GET_ERRORINFO(V_RETURN);



	SELECT 

		A.DATA_ID,

		A.PARENT,

		A.DEPT_CODE,

		A.DEPT_NAME,

		A.BOSS_EMP,

		A.ST_DATE,

		A.END_DATE,

		A.COST_KIND,

		A.DISP_SN,

		A.USE_YN,

		A.INSERT_ID,

		A.INSERT_DT,

		A.UPDATE_ID,

		A.UPDATE_DT,

		A.MODI_KEY,
		
		A.WARE_CODE,

		A.DELETE_YN

    FROM   DEPT_CODE A

    WHERE 	A.DELETE_YN = 'N'

      AND A.USE_YN = 'Y'

   	  and A.WARE_CODE is not null

   	;
   	  
	SET N_RETURN = 0;

    SET V_RETURN = '조회되었습니다.';

end