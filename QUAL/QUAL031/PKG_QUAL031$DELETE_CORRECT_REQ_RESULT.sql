CREATE DEFINER=`ubidom`@`%` PROCEDURE `swmcp`.`PKG_QUAL031$DELETE_CORRECT_REQ_RESULT`(	
	IN A_COMP_ID varchar(10),
	IN A_REQ_DATE TIMESTAMP,
	IN A_REQ_KEY VARCHAR(20),
	OUT N_RETURN INT,
	OUT V_RETURN VARCHAR(4000)
	)
begin

	DECLARE EXIT HANDLER FOR SQLEXCEPTION 
	CALL USP_SYS_GET_ERRORINFO_ALL(V_RETURN, N_RETURN); 

	SET N_RETURN = 0;
  	SET V_RETURN = '저장되었습니다.'; 
  
	DELETE FROM TB_CORRECT_REQ_RESULT
	WHERE COMP_ID = A_COMP_ID
	  AND REQ_DATE = DATE_FORMAT(A_REQ_DATE, '%Y%m%d')
	  AND REQ_KEY = A_REQ_KEY
	 ;
	
	IF ROW_COUNT() = 0 THEN
  	  SET N_RETURN = -1;
      SET V_RETURN = '저장이 실패하였습니다.'; 
  	END IF;  
  
end